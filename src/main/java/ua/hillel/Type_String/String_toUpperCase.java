package ua.hillel.Type_String;

public class String_toUpperCase {
    public static void main(String[] args) {
        String hello = new String (); // оголошення змінною типу String
        System.out.println("hello".toUpperCase()); // метод toUpperCase()
        System.out.println("hello".toLowerCase()); // метод toUpperCase()
    }
}
